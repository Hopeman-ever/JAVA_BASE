public class BinaryTest {
    public static void main(String[] args) {
        //二进制 0b或0B开头
        int n1 = 0b1010;
        //十进制
        int n2 = 1010;
        //八进制 0开头
        int n3 = 01010;
        //十六进制 0x或0X开头
        int n4 = 0x10101;
        System.out.println(n1);
        System.out.println(n2);
        System.out.println(n3);
        System.out.println(n4);

        int a = 222;
    }
}
